class exemplo_01 {

    public static void main ( String [] args){

          System.out.println("Olá Mundo! \n\nEsse é meu primeiro programa em Java.");

          int idade;
          idade = 15; 
         
          System.out.println("idade: " + idade);
          
          //declarar uma variável do tipo String chamada nome:
          String nome;
          nome = "Alecks";
          
          System.out.println("nome: " + nome );

         //declrarar uma variável do tipo boolean chamada status_aluno:
          boolean status_aluno; 
          status_aluno = true; //true = verdadeiro
          System.out.println(status_aluno);
          status_aluno = false; //true = falso
         
         //declarar uma variável do tipo char chamada periodo:
          char periodo;
          periodo = '2';
          System.out.println(periodo);
    }

}